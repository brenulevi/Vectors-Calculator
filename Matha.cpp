#include "Matha.h"
#include <math.h>

/*
Get the number squared
*/
float Matha::Sqrd(float a) { return a * a; }

/*
Get the square root of number
*/
float Matha::Sqrt(float a) { return sqrt(a); }

/*
Get the angle with cosine in radians
*/
float Matha::Acos(float cos) { return acos(cos); }

/*
Get the angle with sine in radians
*/
float Matha::Asin(float sin) { return asin(sin); }